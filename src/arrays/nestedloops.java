package arrays;

public class nestedloops {

	/*1 2 3 4 5
      6 7 8
      9
       */ 
	
	
	
	public static void main(String[] args) {
		
       for(int i=1; i<=4; i++ ) {

      for(int j=1; j<=2*i-1; j++ ) {
    
    	   System.out.print("*");
    	 
       }
       
       
       
       
       System.out.println();
       }
       }

}
